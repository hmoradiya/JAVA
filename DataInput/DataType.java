public class DataType
{
	public static void main (String args[])
	{
		/*
		Data Type	Default Value	Default size
		
		boolean		false			1 bit
		char		'\u0000'		2 byte
		byte		0				1 byte
		short		0				2 byte
		int			0				4 byte
		long		0L				8 byte
		float		0.0f			4 byte
		double		0.0d			8 byte
		*/
		
		int a=10;
		System.out.println(a);
		
		float b =1.2f;
		System.out.println(b);
		
		double c = 9725353553.121d;
		System.out.println(c);
		
		long d = 1234567890L;
		System.out.println(d);
		
		boolean e = true;
		System.out.println(e);
	}
}